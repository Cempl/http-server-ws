{
}


#pragma mark -


/**********************************************************************************************/
void AppendItems( 
	Set_Ptr		inSet,	
	ID_TYPE* 	inArray, 
	vuint32 	inHowMuch,
	bool		inForwardDirection )
{
	vuint32 delta = vuint32(inForwardDirection ? +1 : -1);

	while( inHowMuch-- )
	{
		inSet->Append( *inArray );
		inArray += delta;
	}
}


